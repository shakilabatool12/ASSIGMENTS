//3: Name Cases: Store a person’s name in a variable, and then print that person’s name 
// in lowercase, uppercase, and titlecase.
var firstName1 = "zamal fatima";
console.log(firstName1);
//lower Case
console.log(firstName1.toLowerCase());
//Upper Case
console.log(firstName1.toUpperCase());
//Title Case
console.log(firstName1.charAt(0).toUpperCase() + firstName1.slice(1));
